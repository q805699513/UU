package com.uugty.uu.entity;

public class RoadMarker {
	private String markContent;// 标签内容
	private String markX;// x轴
	private String markY;// y轴

	public String getMarkContent() {
		return markContent;
	}

	public void setMarkContent(String markContent) {
		this.markContent = markContent;
	}

	public String getMarkX() {
		return markX;
	}

	public void setMarkX(String markX) {
		this.markX = markX;
	}

	public String getMarkY() {
		return markY;
	}

	public void setMarkY(String markY) {
		this.markY = markY;
	}

}
